/*
    Method that returns the product of the 13 digit sub number.
*/
int64_t getProduct(char *str, int index)
{
    int i;
    int64_t product = 1;

    for(i = index; i < (index + 13); i++)
    {
        int num = str[i] - '0';
        product *= num;
    }

    return product;
}
